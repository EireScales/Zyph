export default function DashboardNav() {
  const pathname = usePathname();

  return (
    <nav className="px-3">
      {navItems.map(({ href, label, icon: Icon }) => {
        const isActive =
          href === '/dashboard'
            ? pathname === '/dashboard'
            : pathname.startsWith(href);
        return (
          <Link
            key={href}
            href={href}
            className="mb-1 flex items-center gap-2.5 rounded-[10px] px-3 py-2 transition-all duration-200"
            style={{
              fontSize: '0.88rem',
              color: isActive ? 'rgba(255,190,180,1)' : 'rgba(255,255,255,0.45)',
              background: isActive ? 'rgba(232,131,122,0.1)' : 'transparent',
              border: isActive ? '1px solid rgba(232,131,122,0.2)' : '1px solid transparent',
            }}
            onMouseEnter={(e) => {
              if (isActive) return;
              e.currentTarget.style.background = 'rgba(255,255,255,0.04)';
              e.currentTarget.style.color = 'rgba(255,255,255,0.8)';
            }}
            onMouseLeave={(e) => {
              if (isActive) return;
              e.currentTarget.style.background = 'transparent';
              e.currentTarget.style.color = 'rgba(255,255,255,0.45)';
            }}
          >
            <Icon className="h-5 w-5 shrink-0" />
            {label}
          </Link>
        );
      })}
    </nav>
  );
}
